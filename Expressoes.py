#Demonstração do uso de operadores..
x = int(input("Digite um número:"))
y = int(input("Digite outro número:"))

SOMA = x + y
print("O resultado da soma é:", SOMA)

SUBTRACAO = x - y
print("O resultado da subtraçao é:", SUBTRACAO)

MULTIPLICACAO = x * y
print("O resultado da multiplicação é:", MULTIPLICACAO)

DIVISAO = x / y
print("O resultado da didivisão é:", DIVISAO)
